package day20_JavaRcap;

public class WarmUp4 {
	
	/*
	 To print all lower and upper case alphabet from the ASCII table
	 */
	
	public static void main(String[] args) {
		
		// a~z, A~Z
		
		for (char ch = 'a'; ch <= 'z'; ch++) {
			System.out.print(ch);
		}
		
		System.out.println();
		
		for (char ch = 'A'; ch <= 'Z'; ch++) {
			System.out.print(ch);
		}
		
		
	}

}
