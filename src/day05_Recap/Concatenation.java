package day05_Recap;

public class Concatenation 
{
	/*
	 * String: represents the sequence of characters.
	 * Used for storing text.
	 * Surrounded by a double quote.
	 * 
	 * Concatenation: Combining, linking things together.
	 * Creates a new String value.
	 * Done with plus sign.
	 * Can be any primitive datatype.
	 */
	
	public static void main(String[] args) 
	{
		String str = "any text goes here";
		System.out.println(str);
		
		String str2 = "1";
		System.out.println(str2);
		
		String hello = "Hello World";
		System.out.println(hello);
		
		String myName = "Cybertek" + "School";
		System.out.println(myName);
		
		String Year = "This is " + 2019;
		System.out.println(Year);
		
		String name = "Hakan";
		System.out.println("My name is " + name);
		
		int cucumber = 3; int tomato = 5;
		System.out.println("Cucumber is $" + cucumber);
		System.out.println("Tomatoe is $" + tomato);
		
		String newStr = "100" + 10;
		System.out.println(newStr);
		
		System.out.println(1+2+3);
		System.out.println("1"+2+3);
		
		System.out.println("Batch 12" + 1 + 2);
		
		System.out.println(2-1+1+"4");
		
		System.out.println("Batch 12" + (1+2));
		
		
		
	}

}
