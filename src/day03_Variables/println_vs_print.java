package day03_Variables;

public class println_vs_print {

	// the only characters we can give for any name is: _ and $
	
	public static void main(String[] args) {
		
		System.out.println("Hello Cybertek"); // prints from a new line
		System.out.println("School");
		System.out.println("========================");
		
		System.out.print("Hello Cybertek ");
		System.out.println("School"); // printing from where it's left off
		
		System.out.println("========================");
		System.out.print("Today is ");
		System.out.print("a great day");

	}
	
}
